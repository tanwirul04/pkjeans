public static void RefreshData(){
        try {
            jTable1.setRowHeight(30);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            
            for (int i = model.getRowCount()-1; i>=0; i--) {
                model.removeRow(i);   
            }
            
            String Q = "SELECT * FROM produk";
            Connection C = config.configDB();
            Statement S = C.createStatement();
            ResultSet R = S.executeQuery(Q);
            while (R.next()){
                int ID = R.getInt("id_produk");
                String  PRODUK = R.getString("produk");
                String HARGA = R.getString("harga");
                String STOCK = R.getString("stock");
                Object[] datausers = {ID, PRODUK, HARGA, STOCK};
                model.addRow(datausers);
            }
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(SwingConstants.CENTER);
            for (int i = 0; i < jTable1.getColumnCount(); i++) {
            jTable1.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
        }
        } catch (Exception e){
            e.printStackTrace();
            
        }
        
    }
