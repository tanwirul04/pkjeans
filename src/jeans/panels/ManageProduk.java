    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int n = jTable1.getSelectedRow();
        if(n != -1){
            btnEditProduk.setEnabled(true);
            btnHapusProduk.setEnabled(true);
            
            //capture data
            Object IDPg = jTable1.getValueAt(n, 0);
            int ID = Integer.parseInt(IDPg.toString());        
            String produkPd = jTable1.getValueAt(n, 1).toString();
            String hargaPd = jTable1.getValueAt(n, 2).toString();
            String stockPd = jTable1.getValueAt(n, 3).toString();
            Pk = new Produk();
            Pk.setId(ID); 
            Pk.setProduk(produkPd); 
            Pk.setHarga(hargaPd);
            Pk.setStock(stockPd);
        }else {            
        }
    }//GEN-LAST:event_jTable1MouseClicked

